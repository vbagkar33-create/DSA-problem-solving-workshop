import sys
class Queue:

  def __init__(self, queueSize):   # corrected constructor
    self.queueList = []
    self.queueSize = queueSize

  def isFull(self):
      if len(self.queueList) == self.queueSize:
          return True
      else:
          return False

  def Enqueue(self, value):
    if self.isFull():
      print("Queue is full")
    else:
      self.queueList.append(value)
      print(self.queueList)

  def displayQueue(self):
    print("------------------")
    print(self.queueList)
    print("------------------")

  def isEmpty(self):
      if self.queueList == []:
          return True
      else:
          return False

  def Dequeue(self):
      if self.isEmpty():
          return "Queue is empty"
      else:
          return self.queueList.pop(0)

  def deleteQueue(self):
      self.queueList = None
      return "Queue is deleted"

  def peek(self):
      if self.isEmpty():
          return "Queue is empty"
      else:
          return self.queueList[0]


